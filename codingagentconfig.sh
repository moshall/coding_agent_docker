  fi

  echo
  echo "=== cc-connect quick bind ==="
  echo "Config file: ${cfg}"

  local project_name=""
  while true; do
    read -r -p "Project name (English only): " project_name
    project_name="$(trim "${project_name}")"
    if [[ -z "${project_name}" ]]; then
      echo "Project name cannot be empty."
      continue
    fi
    if ! [[ "${project_name}" =~ ^[A-Za-z][A-Za-z0-9_-]*$ ]]; then
      echo "Invalid name. Use English letters, numbers, _ or -; start with a letter."
      continue
    fi
    if cc_project_exists "${cfg}" "${project_name}"; then
      echo "Project already exists in cc-connect config: ${project_name}"
      pause
      return 0
    fi
    break
  done

  local agent_type=""
  local agent_choice=""
  while true; do
    echo
    echo "Agent type:"
    echo "1. claudecode"
    echo "2. codex"
    read -r -p "Select agent [1]: " agent_choice
    agent_choice="$(trim "${agent_choice}")"
    if [[ -z "${agent_choice}" ]]; then
      agent_choice="1"
    fi
    case "${agent_choice}" in
      1)
        agent_type="claudecode"
        break
        ;;
      2)
        agent_type="codex"
        break
        ;;
      *)
        echo "Invalid choice."
        ;;
    esac
  done

  local agent_mode=""
  if [[ "${agent_type}" == "claudecode" ]]; then
    agent_mode="$(select_claudecode_mode)"
  else
    agent_mode="$(select_codex_mode)"
  fi

  local workspace_root="${CLOUDCLI_DEFAULT_WORKSPACE_PATH:-${WORKSPACE_BASE_DEFAULT}}"
  local default_work_dir="${workspace_root}/${project_name}"
  local work_dir=""
  while true; do
    read -r -p "Project directory [${default_work_dir}]: " work_dir
    work_dir="$(trim "${work_dir}")"
    if [[ -z "${work_dir}" ]]; then
      work_dir="${default_work_dir}"
    fi
    if [[ "${work_dir}" != /* ]]; then
      echo "Please use an absolute path."
      continue
    fi
    break
  done

  if [[ ! -d "${work_dir}" ]]; then
    if prompt_yes_no "Directory not found. Create it now? (Y/n):" "Y"; then
      if ! mkdir -p "${work_dir}"; then
        echo "Failed to create directory: ${work_dir}" >&2
        pause
        return 1
      fi
      if [[ "$(id -u)" -eq 0 ]] && id node >/dev/null 2>&1; then
        chown -R node:node "${work_dir}" || true
      fi
    else
      echo "Cancelled: directory must exist for binding."
      pause
      return 0
    fi
  fi

  local admin_from=""
  read -r -p "Admin users (optional, comma IDs; * = all): " admin_from
  admin_from="$(trim "${admin_from}")"

  local platform_type=""
  local platform_choice=""
  while true; do
    echo
    echo "Chat channel:"
    echo "1. Telegram"
    echo "2. Discord"
    echo "3. Feishu"
    read -r -p "Select channel: " platform_choice
    platform_choice="$(trim "${platform_choice}")"
    case "${platform_choice}" in
      1)
        platform_type="telegram"
        break
        ;;
      2)
        platform_type="discord"
        break
        ;;
      3)
        platform_type="feishu"
        break
        ;;
      *)
        echo "Invalid choice."
        ;;
    esac
  done

  local allow_from=""
  local secret_a=""
  local secret_b=""
  local guild_id=""
  local thread_isolation="false"
  local enable_feishu_card="true"

  case "${platform_type}" in
    telegram)
      secret_a="$(read_required_input "Telegram bot token: " 1)"
      read -r -p "Allow users (optional, comma IDs; default *): " allow_from
      allow_from="$(trim "${allow_from}")"
      if [[ -z "${allow_from}" ]]; then
        allow_from="*"
      fi
      ;;
    discord)
      secret_a="$(read_required_input "Discord bot token: " 1)"
      read -r -p "Discord guild_id (optional, recommend for slash command): " guild_id
      guild_id="$(trim "${guild_id}")"
      read -r -p "Allow users (optional, comma IDs; default *): " allow_from
      allow_from="$(trim "${allow_from}")"
      if [[ -z "${allow_from}" ]]; then
        allow_from="*"
      fi
      if prompt_yes_no "Enable thread isolation? (y/N):" "N"; then
        thread_isolation="true"
      fi
      ;;
    feishu)
      secret_a="$(read_required_input "Feishu app_id: " 0)"
      secret_b="$(read_required_input "Feishu app_secret: " 1)"
      read -r -p "Allow users (optional open_id list; default *): " allow_from
      allow_from="$(trim "${allow_from}")"
      if [[ -z "${allow_from}" ]]; then
        allow_from="*"
      fi
      if prompt_yes_no "Enable Feishu card replies? (Y/n):" "Y"; then
        enable_feishu_card="true"
      else
        enable_feishu_card="false"
      fi
      ;;
  esac

  echo
  echo "=== Confirm cc-connect binding ==="
  echo "Project: ${project_name}"
  echo "Agent: ${agent_type} (mode=${agent_mode})"
  echo "Work dir: ${work_dir}"
  echo "Platform: ${platform_type}"
  if [[ -n "${admin_from}" ]]; then
    echo "Admin users: ${admin_from}"
  else
    echo "Admin users: (not set)"
  fi
  echo "Allow users: ${allow_from}"
  echo "Config file: ${cfg}"

  if ! prompt_yes_no "Write this project into config.toml? (Y/n):" "Y"; then
    echo "Cancelled."
    pause
    return 0
  fi

  if ! append_cc_connect_project \
    "${cfg}" \
    "${project_name}" \
    "${agent_type}" \
    "${agent_mode}" \
    "${work_dir}" \
    "${platform_type}" \
    "${admin_from}" \
    "${allow_from}" \
    "${secret_a}" \
    "${secret_b}" \
    "${guild_id}" \
    "${thread_isolation}" \
    "${enable_feishu_card}"; then
    echo "Failed to update config file: ${cfg}" >&2
    pause
    return 1
  fi

  echo "Project binding added to: ${cfg}"
  echo "Next step: run 'cc-connect -config ${cfg}' to start."
  pause
}

menu_cc_connect_self_check() {
  HC_PASS=0
  HC_FAIL=0
  HC_SKIP=0

  local cfg
  cfg="$(cc_connect_config_path)"

  echo
  echo "=== cc-connect connection self-check ==="
  echo "Config file: ${cfg}"

  if [[ -f "${cfg}" ]]; then
    health_pass "config file exists"
  else
    health_fail "config file not found"
  fi
  if [[ -r "${cfg}" ]]; then
    health_pass "config file readable"
  else
    health_fail "config file not readable"
  fi

  if [[ -r "${cfg}" ]]; then
    local project_count
    project_count="$(grep -Ec '^[[:space:]]*\[\[projects\]\][[:space:]]*$' "${cfg}" || true)"
    if [[ "${project_count}" -gt 0 ]]; then
      health_pass "project blocks: ${project_count}"
    else
      health_fail "project blocks: 0"
    fi

    local workdir_count
    workdir_count="$(grep -Ec '^[[:space:]]*work_dir[[:space:]]*=' "${cfg}" || true)"
    if [[ "${workdir_count}" -gt 0 ]]; then
      health_pass "work_dir entries: ${workdir_count}"
    else
      health_fail "work_dir entries: 0"
    fi

    local wd
    while IFS= read -r wd; do
      wd="$(trim "${wd}")"
      if [[ -z "${wd}" ]]; then
        continue
      fi
      if [[ -d "${wd}" ]]; then
        health_pass "work_dir exists: ${wd}"
      else
        health_fail "work_dir missing: ${wd}"
      fi
    done < <(sed -n 's/^[[:space:]]*work_dir[[:space:]]*=[[:space:]]*"\(.*\)"[[:space:]]*$/\1/p' "${cfg}")

    cc_validate_platform_credentials "${cfg}" || true
  else
    health_skip "skip credentials/work_dir checks (config unreadable)"
  fi

  if have_cmd cc-connect; then
    health_check "cc-connect CLI" "cc-connect --version >/dev/null 2>&1"
  else
    health_fail "cc-connect CLI not found"
  fi

  local cc_process_up="0"
  if pgrep -x cc-connect >/dev/null 2>&1; then
    cc_process_up="1"
    health_pass "cc-connect process"
  else
    health_fail "cc-connect process"
  fi

  local cc_port="${CC_CONNECT_PORT:-8080}"
  local cc_port_up="0"
  if port_is_listening "${cc_port}"; then
    cc_port_up="1"
  fi

  if [[ "${cc_process_up}" == "1" && "${cc_port_up}" == "1" ]]; then
    health_pass "cc-connect listening on :${cc_port}"
  elif [[ "${cc_process_up}" == "1" && "${cc_port_up}" == "0" ]]; then
    health_fail "cc-connect listening on :${cc_port} (process up but port not open)"
  elif [[ "${cc_process_up}" == "0" && "${cc_port_up}" == "1" ]]; then
    health_fail "cc-connect listening on :${cc_port} (port occupied by another process)"
  else
    health_skip "cc-connect listening on :${cc_port} (service not running)"
  fi

  echo "Summary: pass=${HC_PASS} fail=${HC_FAIL} skip=${HC_SKIP}"
  pause
}

menu_cc_connect_service_control() {
  while true; do
    local cfg
    cfg="$(cc_connect_config_path)"
    local cc_port="${CC_CONNECT_PORT:-8080}"
    local log_path
    log_path="$(cc_connect_log_path)"

    echo
    echo "=== cc-connect service control ==="
    echo "Config: ${cfg}"
    echo "Port: ${cc_port}"
    echo "Log: ${log_path}"
    if cc_connect_is_running; then
      echo "Status: running"
    else
      echo "Status: stopped"
    fi
    echo "1. Start service"
    echo "2. Stop service"
    echo "3. Restart service"
    echo "4. Show recent logs"
    echo "0. Back"
    echo
    read -r -p "Select an option: " action

    case "${action}" in
      1)
        cc_connect_start_service || true
        pause
        ;;
      2)
        cc_connect_stop_service || true
        pause
        ;;
      3)
        cc_connect_stop_service || true
        cc_connect_start_service || true
        pause
        ;;
      4)
        if [[ -r "${log_path}" ]]; then
          echo "--- tail -n 80 ${log_path} ---"
          tail -n 80 "${log_path}" || true
        else
          echo "Log file not readable: ${log_path}"
        fi
        pause
        ;;
      0)
        return 0
        ;;
      *)
        echo "Invalid choice."
        ;;
    esac
  done
}

