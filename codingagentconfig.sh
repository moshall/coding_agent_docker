}

trim() {
  local s="$1"
  s="${s#"${s%%[![:space:]]*}"}"
  s="${s%"${s##*[![:space:]]}"}"
  printf '%s' "${s}"
}

normalize_semver() {
  local v="${1:-}"
  v="$(trim "${v}")"
  v="${v#v}"
  printf '%s' "${v}"
}

extract_semver() {
  local raw="${1:-}"
  printf '%s' "${raw}" | grep -Eo 'v?[0-9]+\.[0-9]+\.[0-9]+' | head -n 1 | sed 's/^v//'
}

have_cmd() {
  command -v "$1" >/dev/null 2>&1
}

HC_PASS=0
HC_FAIL=0
HC_SKIP=0

health_pass() {
  HC_PASS=$((HC_PASS + 1))
  echo "PASS: $*"
}

health_fail() {
  HC_FAIL=$((HC_FAIL + 1))
  echo "FAIL: $*"
}

health_skip() {
  HC_SKIP=$((HC_SKIP + 1))
  echo "SKIP: $*"
}

health_check() {
  local name="$1"
  local cmd="$2"
  if bash -lc "${cmd}" >/dev/null 2>&1; then
    health_pass "${name}"
  else
    health_fail "${name}"
  fi
}

toml_escape() {
  local s="${1:-}"
  s="${s//\\/\\\\}"
  s="${s//\"/\\\"}"
  printf '%s' "${s}"
}

prompt_yes_no() {
  local prompt="$1"
  local default="${2:-N}"
  local answer=""
  read -r -p "${prompt} " answer
  answer="$(trim "${answer}")"
  if [[ -z "${answer}" ]]; then
    answer="${default}"
  fi
  [[ "${answer}" =~ ^[Yy]$ ]]
}

cc_connect_config_path() {
