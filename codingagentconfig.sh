mask_secret() {
  local raw="$1"
  local n="${#raw}"
  if (( n == 0 )); then
    printf '%s' "(empty)"
  elif (( n <= 8 )); then
    printf '%s' "****"
  else
    printf '%s' "${raw:0:4}****${raw:n-2:2}"
  fi
}

cc_connect_project_exists_except_index() {
  local cfg="$1"
