}

trim() {
  local s="$1"
  s="${s#"${s%%[![:space:]]*}"}"
  s="${s%"${s##*[![:space:]]}"}"
  printf '%s' "${s}"
}

normalize_semver() {
  local v="${1:-}"
  v="$(trim "${v}")"
  v="${v#v}"
  printf '%s' "${v}"
}

extract_semver() {
  local raw="${1:-}"
  printf '%s' "${raw}" | grep -Eo 'v?[0-9]+\.[0-9]+\.[0-9]+' | head -n 1 | sed 's/^v//'
}

