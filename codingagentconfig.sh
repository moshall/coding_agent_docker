cc_block_key_in_header() {
  local block="$1"
  local header="$2"
  local key="$3"
  printf '%s\n' "${block}" | awk -v header="${header}" -v key="${key}" '
    BEGIN { inside=0 }
    {
      line = $0
      sub(/#.*/, "", line)
      gsub(/\r/, "", line)
      trimmed = line
      sub(/^[ \t]+/, "", trimmed)
      sub(/[ \t]+$/, "", trimmed)
      if (trimmed == header) {
        inside = 1
        next
      }
      if (inside && trimmed ~ /^\[/) {
        inside = 0
      }
      if (!inside) {
        next
      }
      pattern = "^[ \t]*" key "[ \t]*=[ \t]*\""
      if (line ~ pattern) {
        sub(pattern, "", line)
        sub(/"[ \t]*$/, "", line)
        print line
        exit
      }
    }
  '
}

