      echo "Status: running"
    else
      echo "Status: stopped"
    fi
    echo "1. Start service"
    echo "2. Stop service"
    echo "3. Restart service"
    echo "4. Show recent logs"
    echo "0. Back"
    echo
    read -r -p "Select an option: " action

    case "${action}" in
      1)
        cc_connect_start_service || true
        pause
        ;;
      2)
        cc_connect_stop_service || true
        pause
        ;;
      3)
        cc_connect_stop_service || true
        cc_connect_start_service || true
