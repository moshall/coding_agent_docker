cc_validate_platform_credentials() {
  local cfg="$1"
  local line=""
  local clean=""
  local in_platform_block="0"
  local platform_type=""
  local has_token="0"
  local has_app_id="0"
  local has_app_secret="0"
  local checked_blocks=0
  local missing_blocks=0
  local tg_idx=0
  local dc_idx=0
  local fs_idx=0

  finalize_platform_block() {
    local label=""
    if [[ "${in_platform_block}" != "1" || -z "${platform_type}" ]]; then
      return 0
    fi

    case "${platform_type}" in
      telegram)
        tg_idx=$((tg_idx + 1))
        label="Credentials: telegram#${tg_idx}"
        checked_blocks=$((checked_blocks + 1))
        if [[ "${has_token}" == "1" ]]; then
          health_pass "${label}"
        else
          missing_blocks=$((missing_blocks + 1))
          health_fail "${label} missing token"
        fi
        ;;
      discord)
        dc_idx=$((dc_idx + 1))
        label="Credentials: discord#${dc_idx}"
        checked_blocks=$((checked_blocks + 1))
        if [[ "${has_token}" == "1" ]]; then
          health_pass "${label}"
        else
          missing_blocks=$((missing_blocks + 1))
          health_fail "${label} missing token"
        fi
        ;;
      feishu)
        fs_idx=$((fs_idx + 1))
        label="Credentials: feishu#${fs_idx}"
        checked_blocks=$((checked_blocks + 1))
        if [[ "${has_app_id}" == "1" && "${has_app_secret}" == "1" ]]; then
          health_pass "${label}"
        else
          missing_blocks=$((missing_blocks + 1))
          health_fail "${label} missing app_id/app_secret"
        fi
        ;;
    esac
  }

  while IFS= read -r line || [[ -n "${line}" ]]; do
    clean="${line%%#*}"
    clean="$(trim "${clean}")"
    if [[ -z "${clean}" ]]; then
      continue
    fi

    if [[ "${clean}" == "[[projects.platforms]]" || "${clean}" == "[[projects]]" ]]; then
      finalize_platform_block
      has_token="0"
      has_app_id="0"
      has_app_secret="0"
      platform_type=""
      if [[ "${clean}" == "[[projects.platforms]]" ]]; then
        in_platform_block="1"
      else
        in_platform_block="0"
      fi
      continue
    fi

    if [[ "${in_platform_block}" != "1" ]]; then
      continue
    fi

    if [[ "${clean}" =~ ^type[[:space:]]*=[[:space:]]*\"([A-Za-z0-9_-]+)\"$ ]]; then
      platform_type="${BASH_REMATCH[1]}"
      continue
    fi
    if [[ "${clean}" =~ ^token[[:space:]]*=[[:space:]]*\".+\"$ ]]; then
      has_token="1"
      continue
    fi
    if [[ "${clean}" =~ ^app_id[[:space:]]*=[[:space:]]*\".+\"$ ]]; then
      has_app_id="1"
      continue
    fi
    if [[ "${clean}" =~ ^app_secret[[:space:]]*=[[:space:]]*\".+\"$ ]]; then
      has_app_secret="1"
      continue
    fi
  done <"${cfg}"

  finalize_platform_block

  if [[ "${checked_blocks}" -eq 0 ]]; then
    health_fail "Credentials: no Telegram/Discord/Feishu platform block found"
    return 1
  fi
  if [[ "${missing_blocks}" -gt 0 ]]; then
    return 1
  fi
  return 0
}

npm_current_version() {
  local pkg="$1"
  if ! have_cmd npm; then
    return 0
  fi
  local raw
  raw="$(npm ls -g "${pkg}" --depth=0 --json 2>/dev/null || true)"
  printf '%s' "${raw}" | node -e '
let data = "";
process.stdin.on("data", (c) => data += c);
process.stdin.on("end", () => {
  try {
    const json = JSON.parse(data);
    const pkg = process.argv[1];
    const v = json?.dependencies?.[pkg]?.version || "";
    process.stdout.write(v);
  } catch {}
});
' "${pkg}"
}

npm_latest_version() {
  local pkg="$1"
  if ! have_cmd npm; then
    return 0
  fi
  npm view "${pkg}" version 2>/dev/null | tr -d '[:space:]'
}

cc_connect_current_version() {
  local out=""
  if have_cmd cc-connect; then
    out="$(cc-connect --version 2>/dev/null || true)"
    if [[ -z "${out}" ]]; then
      out="$(cc-connect --help 2>&1 | head -n 5 || true)"
    fi
  fi
  extract_semver "${out}"
}

cc_connect_latest_version() {
  if ! have_cmd git; then
    return 0
  fi
  git ls-remote --tags --refs https://github.com/chenhg5/cc-connect.git "refs/tags/v*" 2>/dev/null \
    | awk -F/ '{print $3}' \
    | sed 's/^v//' \
    | sort -V \
    | tail -n 1
}

detect_tool_versions() {
  local i
  for i in "${!TOOLS[@]}"; do
    IFS='|' read -r display key source <<<"${TOOLS[$i]}"
    local current=""
    local latest=""
    local status="unknown"
    local can_update="0"

    if [[ "${source}" == "npm" ]]; then
      current="$(normalize_semver "$(npm_current_version "${key}")")"
      latest="$(normalize_semver "$(npm_latest_version "${key}")")"
    else
      current="$(normalize_semver "$(cc_connect_current_version)")"
      latest="$(normalize_semver "$(cc_connect_latest_version)")"
    fi

    if [[ -n "${current}" && -n "${latest}" ]]; then
      if [[ "${current}" != "${latest}" ]]; then
        status="update"
        can_update="1"
      else
        status="latest"
      fi
    elif [[ -n "${current}" && -z "${latest}" ]]; then
      status="offline"
    elif [[ -z "${current}" && -n "${latest}" ]]; then
      status="unknown_current"
      can_update="1"
    else
      status="unknown"
    fi

    TOOL_CURRENT[$i]="${current}"
    TOOL_LATEST[$i]="${latest}"
    TOOL_STATUS[$i]="${status}"
    TOOL_CAN_UPDATE[$i]="${can_update}"
  done
}

tool_suffix() {
  local idx="$1"
  local status="${TOOL_STATUS[$idx]:-unknown}"
  case "${status}" in
    update)
      printf '%s' " (update available)"
      ;;
    latest)
      printf '%s' " (latest)"
      ;;
    offline)
      printf '%s' " (latest check unavailable)"
      ;;
    unknown_current)
      printf '%s' " (current unknown)"
      ;;
    *)
      printf '%s' ""
      ;;
  esac
}

print_tool_detail() {
  local idx="$1"
  local display source key
  IFS='|' read -r display key source <<<"${TOOLS[$idx]}"
  echo
  echo "Tool: ${display}"
  echo "Current: ${TOOL_CURRENT[$idx]:-unknown}"
  echo "Latest: ${TOOL_LATEST[$idx]:-unknown}"
}

update_npm_tool() {
  local pkg="$1"
  echo "Updating ${pkg}..."
  if npm install -g "${pkg}@latest"; then
    echo "Update completed: ${pkg}"
    return 0
  fi
  echo "Update failed for ${pkg}. Check permission and network." >&2
  return 1
}

update_cc_connect() {
  local latest_tag
  latest_tag="$(git ls-remote --tags --refs https://github.com/chenhg5/cc-connect.git "refs/tags/v*" 2>/dev/null | awk -F/ '{print $3}' | sort -V | tail -n 1 || true)"
  if [[ -z "${latest_tag}" ]]; then
    echo "Unable to get latest cc-connect tag." >&2
    return 1
  fi

  if ! have_cmd go; then
    echo "go is required to update cc-connect from source." >&2
    echo "Set INSTALL_GO_RUNTIME=true and recreate container, then retry." >&2
    return 1
  fi

  local tmpdir
  tmpdir="$(mktemp -d)"
  local outbin="${tmpdir}/cc-connect.new"

  echo "Building cc-connect from ${latest_tag}..."
  if ! git clone --depth 1 --branch "${latest_tag}" https://github.com/chenhg5/cc-connect.git "${tmpdir}/src" >/dev/null 2>&1; then
    rm -rf "${tmpdir}"
    echo "Failed to clone cc-connect source." >&2
    return 1
  fi

  (
    set -e
    cd "${tmpdir}/src"
    go build -ldflags='-w -s' -o "${outbin}" ./cmd/cc-connect || \
    go build -ldflags='-w -s' -o "${outbin}" . || \
    go build -ldflags='-w -s' -o "${outbin}" ./...
  ) >/dev/null 2>&1 || {
    rm -rf "${tmpdir}"
    echo "Failed to build cc-connect." >&2
    return 1
  }

  if [[ "$(id -u)" -eq 0 ]]; then
    install -m 0755 "${outbin}" /usr/local/bin/cc-connect
  elif have_cmd sudo; then
    sudo install -m 0755 "${outbin}" /usr/local/bin/cc-connect
  else
    rm -rf "${tmpdir}"
    echo "Need root privilege to install /usr/local/bin/cc-connect." >&2
    return 1
  fi

  rm -rf "${tmpdir}"
  echo "cc-connect updated to ${latest_tag}"
  return 0
}

update_tool_by_index() {
  local idx="$1"
  local display key source
  IFS='|' read -r display key source <<<"${TOOLS[$idx]}"

  if [[ "${TOOL_CAN_UPDATE[$idx]:-0}" != "1" ]]; then
    echo "No update available for ${display}."
    return 0
  fi

  if [[ "${source}" == "npm" ]]; then
    update_npm_tool "${key}"
  else
    update_cc_connect
  fi
}

menu_check_update() {
  while true; do
    detect_tool_versions
    echo
    echo "=== Check Updates ==="
    local i
    for i in "${!TOOLS[@]}"; do
      IFS='|' read -r display _ _ <<<"${TOOLS[$i]}"
      echo "$((i + 1)). ${display}$(tool_suffix "${i}")"
    done
    echo "0. Back"
    echo
    read -r -p "Select a tool: " choice

    if [[ "${choice}" == "0" ]]; then
      return 0
    fi
    if ! [[ "${choice}" =~ ^[1-5]$ ]]; then
      echo "Invalid choice."
      continue
    fi

    local idx=$((choice - 1))
    print_tool_detail "${idx}"
    if [[ "${TOOL_CAN_UPDATE[$idx]:-0}" == "1" ]]; then
      read -r -p "Update now? (y/N): " confirm
      if [[ "${confirm}" =~ ^[Yy]$ ]]; then
        update_tool_by_index "${idx}" || true
      fi
    else
      echo "No update action needed."
    fi
    pause
  done
}

menu_create_workspace() {
  local base_dir="${CLOUDCLI_DEFAULT_WORKSPACE_PATH:-${WORKSPACE_BASE_DEFAULT}}"
  echo
  echo "Workspace base: ${base_dir}"
  read -r -p "Enter workspace name (English only): " ws_name
  ws_name="$(trim "${ws_name}")"

  if [[ -z "${ws_name}" ]]; then
    echo "Workspace name cannot be empty."
    pause
    return 0
  fi

  if ! [[ "${ws_name}" =~ ^[A-Za-z][A-Za-z0-9_-]*$ ]]; then
    echo "Invalid name. Use English letters, numbers, _ or -; start with a letter."
    pause
    return 0
  fi

  if [[ ! -d "${base_dir}" ]]; then
    echo "Workspace base does not exist: ${base_dir}" >&2
    pause
    return 1
  fi

  local target="${base_dir}/${ws_name}"
  if [[ -e "${target}" ]]; then
    echo "Workspace already exists: ${target}"
    pause
    return 0
  fi

  if mkdir -p "${target}"; then
    if [[ "$(id -u)" -eq 0 ]] && id node >/dev/null 2>&1; then
      chown -R node:node "${target}" || true
    fi
    echo "Workspace created: ${target}"
  else
    echo "Failed to create workspace: ${target}" >&2
  fi
  pause
}

menu_health_status() {
  HC_PASS=0
  HC_FAIL=0
  HC_SKIP=0

  echo
  echo "=== Health Status ==="

  health_check "pid1 process exists" "ps -p 1 >/dev/null"
  health_check "cron process" "pgrep -x cron >/dev/null"

  if have_cmd tailscaled; then
    if [[ -e /dev/net/tun ]]; then
      health_check "tailscaled process" "pgrep -x tailscaled >/dev/null"
    else
      health_skip "tailscaled process (/dev/net/tun missing)"
    fi
  else
    health_skip "tailscaled process (tailscaled not installed)"
  fi

  local cloudcli_enable_lc
  cloudcli_enable_lc="$(printf '%s' "${CLOUDCLI_ENABLE:-true}" | tr '[:upper:]' '[:lower:]')"
  local cloudcli_port="${CLOUDCLI_PORT:-3001}"

  if [[ "${cloudcli_enable_lc}" == "false" || "${cloudcli_enable_lc}" == "0" || "${cloudcli_enable_lc}" == "no" || "${cloudcli_enable_lc}" == "off" ]]; then
    health_skip "cloudcli checks (CLOUDCLI_ENABLE=${CLOUDCLI_ENABLE:-false})"
  else
    health_check "cloudcli CLI" "cloudcli version >/dev/null 2>&1"
    health_check "cloudcli listening" "if command -v ss >/dev/null 2>&1; then ss -ltn 2>/dev/null | awk '{print \$4}' | grep -Eq '[:.]${cloudcli_port}\$'; elif command -v netstat >/dev/null 2>&1; then netstat -ltn 2>/dev/null | awk '{print \$4}' | grep -Eq '[:.]${cloudcli_port}\$'; elif command -v python3 >/dev/null 2>&1; then python3 -c 'import socket,sys; s=socket.socket(socket.AF_INET, socket.SOCK_STREAM); s.settimeout(0.5); rc=s.connect_ex((\"127.0.0.1\", int(sys.argv[1]))); s.close(); raise SystemExit(0 if rc == 0 else 1)' '${cloudcli_port}'; else false; fi"
    health_check "cloudcli HTTP" "curl -fsS 'http://127.0.0.1:${cloudcli_port}/' >/dev/null"
  fi

  echo "Summary: pass=${HC_PASS} fail=${HC_FAIL} skip=${HC_SKIP}"
  pause
}

