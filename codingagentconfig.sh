}

prompt_project_index() {
  local cfg="$1"
  local prompt_text="$2"
  local pick=""
  while true; do
    read -r -p "${prompt_text}" pick
    pick="$(trim "${pick}")"
    if [[ -z "${pick}" ]]; then
      return 1
    fi
    if ! [[ "${pick}" =~ ^[0-9]+$ ]]; then
      echo "Invalid number."
      continue
    fi
    if cc_connect_get_project_range "${cfg}" "${pick}" >/dev/null; then
      printf '%s' "${pick}"
      return 0
    fi
    echo "Project index not found."
  done
}

cc_validate_platform_credentials() {
  local cfg="$1"
  local line=""
  local clean=""
  local in_platform_block="0"
  local platform_type=""
  local has_token="0"
  local has_app_id="0"
  local has_app_secret="0"
  local checked_blocks=0
  local missing_blocks=0
  local tg_idx=0
  local dc_idx=0
  local fs_idx=0

  finalize_platform_block() {
    local label=""
    if [[ "${in_platform_block}" != "1" || -z "${platform_type}" ]]; then
      return 0
    fi

    case "${platform_type}" in
      telegram)
        tg_idx=$((tg_idx + 1))
        label="Credentials: telegram#${tg_idx}"
        checked_blocks=$((checked_blocks + 1))
        if [[ "${has_token}" == "1" ]]; then
          health_pass "${label}"
        else
          missing_blocks=$((missing_blocks + 1))
          health_fail "${label} missing token"
        fi
        ;;
      discord)
        dc_idx=$((dc_idx + 1))
        label="Credentials: discord#${dc_idx}"
        checked_blocks=$((checked_blocks + 1))
        if [[ "${has_token}" == "1" ]]; then
          health_pass "${label}"
        else
          missing_blocks=$((missing_blocks + 1))
          health_fail "${label} missing token"
        fi
        ;;
      feishu)
        fs_idx=$((fs_idx + 1))
        label="Credentials: feishu#${fs_idx}"
        checked_blocks=$((checked_blocks + 1))
        if [[ "${has_app_id}" == "1" && "${has_app_secret}" == "1" ]]; then
          health_pass "${label}"
        else
          missing_blocks=$((missing_blocks + 1))
          health_fail "${label} missing app_id/app_secret"
        fi
        ;;
    esac
  }

  while IFS= read -r line || [[ -n "${line}" ]]; do
    clean="${line%%#*}"
    clean="$(trim "${clean}")"
    if [[ -z "${clean}" ]]; then
      continue
    fi

    if [[ "${clean}" == "[[projects.platforms]]" || "${clean}" == "[[projects]]" ]]; then
      finalize_platform_block
      has_token="0"
      has_app_id="0"
      has_app_secret="0"
      platform_type=""
      if [[ "${clean}" == "[[projects.platforms]]" ]]; then
        in_platform_block="1"
      else
        in_platform_block="0"
      fi
      continue
    fi

    if [[ "${in_platform_block}" != "1" ]]; then
      continue
    fi

    if [[ "${clean}" =~ ^type[[:space:]]*=[[:space:]]*\"([A-Za-z0-9_-]+)\"$ ]]; then
      platform_type="${BASH_REMATCH[1]}"
      continue
    fi
    if [[ "${clean}" =~ ^token[[:space:]]*=[[:space:]]*\".+\"$ ]]; then
      has_token="1"
      continue
    fi
    if [[ "${clean}" =~ ^app_id[[:space:]]*=[[:space:]]*\".+\"$ ]]; then
      has_app_id="1"
      continue
    fi
    if [[ "${clean}" =~ ^app_secret[[:space:]]*=[[:space:]]*\".+\"$ ]]; then
      has_app_secret="1"
      continue
    fi
  done <"${cfg}"

  finalize_platform_block

  if [[ "${checked_blocks}" -eq 0 ]]; then
    health_fail "Credentials: no Telegram/Discord/Feishu platform block found"
    return 1
  fi
  if [[ "${missing_blocks}" -gt 0 ]]; then
    return 1
  fi
  return 0
}

npm_current_version() {
  local pkg="$1"
  if ! have_cmd npm; then
    return 0
  fi
  local raw
  raw="$(npm ls -g "${pkg}" --depth=0 --json 2>/dev/null || true)"
  printf '%s' "${raw}" | node -e '
let data = "";
process.stdin.on("data", (c) => data += c);
process.stdin.on("end", () => {
  try {
    const json = JSON.parse(data);
    const pkg = process.argv[1];
    const v = json?.dependencies?.[pkg]?.version || "";
    process.stdout.write(v);
  } catch {}
});
' "${pkg}"
}

npm_latest_version() {
  local pkg="$1"
  if ! have_cmd npm; then
    return 0
  fi
  npm view "${pkg}" version 2>/dev/null | tr -d '[:space:]'
}

cc_connect_current_version() {
  local out=""
  if have_cmd cc-connect; then
    out="$(cc-connect --version 2>/dev/null || true)"
    if [[ -z "${out}" ]]; then
      out="$(cc-connect --help 2>&1 | head -n 5 || true)"
    fi
  fi
  extract_semver "${out}"
}

cc_connect_latest_version() {
  if ! have_cmd git; then
    return 0
  fi
  git ls-remote --tags --refs https://github.com/chenhg5/cc-connect.git "refs/tags/v*" 2>/dev/null \
    | awk -F/ '{print $3}' \
    | sed 's/^v//' \
    | sort -V \
    | tail -n 1
}

detect_tool_versions() {
  local i
  for i in "${!TOOLS[@]}"; do
    IFS='|' read -r display key source <<<"${TOOLS[$i]}"
    local current=""
    local latest=""
    local status="unknown"
    local can_update="0"

    if [[ "${source}" == "npm" ]]; then
      current="$(normalize_semver "$(npm_current_version "${key}")")"
      latest="$(normalize_semver "$(npm_latest_version "${key}")")"
    else
      current="$(normalize_semver "$(cc_connect_current_version)")"
      latest="$(normalize_semver "$(cc_connect_latest_version)")"
    fi

    if [[ -n "${current}" && -n "${latest}" ]]; then
      if [[ "${current}" != "${latest}" ]]; then
        status="update"
        can_update="1"
      else
        status="latest"
      fi
    elif [[ -n "${current}" && -z "${latest}" ]]; then
      status="offline"
    elif [[ -z "${current}" && -n "${latest}" ]]; then
      status="unknown_current"
      can_update="1"
    else
      status="unknown"
    fi

    TOOL_CURRENT[$i]="${current}"
    TOOL_LATEST[$i]="${latest}"
    TOOL_STATUS[$i]="${status}"
    TOOL_CAN_UPDATE[$i]="${can_update}"
  done
}

tool_suffix() {
  local idx="$1"
  local status="${TOOL_STATUS[$idx]:-unknown}"
  case "${status}" in
    update)
      printf '%s' " (update available)"
      ;;
    latest)
      printf '%s' " (latest)"
      ;;
    offline)
      printf '%s' " (latest check unavailable)"
      ;;
    unknown_current)
      printf '%s' " (current unknown)"
      ;;
    *)
      printf '%s' ""
      ;;
  esac
}

print_tool_detail() {
