  local exclude_idx="$3"
  local idx _ _ existing_name
  while IFS='|' read -r idx _ _ existing_name; do
    if [[ "${idx}" != "${exclude_idx}" && "${existing_name}" == "${name}" ]]; then
      return 0
    fi
  done < <(cc_connect_project_ranges "${cfg}")
