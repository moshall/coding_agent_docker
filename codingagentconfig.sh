menu_cc_connect_self_check() {
  HC_PASS=0
  HC_FAIL=0
  HC_SKIP=0

  local cfg
  cfg="$(cc_connect_config_path)"

  echo
  echo "=== cc-connect connection self-check ==="
  echo "Config file: ${cfg}"

  if [[ -f "${cfg}" ]]; then
    health_pass "config file exists"
  else
    health_fail "config file not found"
  fi
  if [[ -r "${cfg}" ]]; then
    health_pass "config file readable"
  else
    health_fail "config file not readable"
  fi

  if [[ -r "${cfg}" ]]; then
    local project_count
    project_count="$(grep -Ec '^[[:space:]]*\[\[projects\]\][[:space:]]*$' "${cfg}" || true)"
    if [[ "${project_count}" -gt 0 ]]; then
      health_pass "project blocks: ${project_count}"
    else
      health_fail "project blocks: 0"
    fi

    local workdir_count
    workdir_count="$(grep -Ec '^[[:space:]]*work_dir[[:space:]]*=' "${cfg}" || true)"
    if [[ "${workdir_count}" -gt 0 ]]; then
      health_pass "work_dir entries: ${workdir_count}"
    else
      health_fail "work_dir entries: 0"
    fi

    local wd
    while IFS= read -r wd; do
      wd="$(trim "${wd}")"
      if [[ -z "${wd}" ]]; then
        continue
      fi
      if [[ -d "${wd}" ]]; then
        health_pass "work_dir exists: ${wd}"
      else
        health_fail "work_dir missing: ${wd}"
      fi
    done < <(sed -n 's/^[[:space:]]*work_dir[[:space:]]*=[[:space:]]*"\(.*\)"[[:space:]]*$/\1/p' "${cfg}")

    cc_validate_platform_credentials "${cfg}" || true
  else
    health_skip "skip credentials/work_dir checks (config unreadable)"
  fi

  if have_cmd cc-connect; then
    health_check "cc-connect CLI" "cc-connect --version >/dev/null 2>&1"
  else
    health_fail "cc-connect CLI not found"
  fi

  local cc_process_up="0"
  if pgrep -x cc-connect >/dev/null 2>&1; then
    cc_process_up="1"
    health_pass "cc-connect process"
  else
    health_fail "cc-connect process"
  fi

  local cc_port="${CC_CONNECT_PORT:-8080}"
  local cc_port_up="0"
  if port_is_listening "${cc_port}"; then
    cc_port_up="1"
  fi

  if [[ "${cc_process_up}" == "1" && "${cc_port_up}" == "1" ]]; then
    health_pass "cc-connect listening on :${cc_port}"
  elif [[ "${cc_process_up}" == "1" && "${cc_port_up}" == "0" ]]; then
    health_fail "cc-connect listening on :${cc_port} (process up but port not open)"
  elif [[ "${cc_process_up}" == "0" && "${cc_port_up}" == "1" ]]; then
    health_fail "cc-connect listening on :${cc_port} (port occupied by another process)"
  else
    health_skip "cc-connect listening on :${cc_port} (service not running)"
  fi

  echo "Summary: pass=${HC_PASS} fail=${HC_FAIL} skip=${HC_SKIP}"
  pause
}

