  echo
  echo "Tool: ${display}"
  echo "Current: ${TOOL_CURRENT[$idx]:-unknown}"
  echo "Latest: ${TOOL_LATEST[$idx]:-unknown}"
}

update_npm_tool() {
  local pkg="$1"
  echo "Updating ${pkg}..."
  if npm install -g "${pkg}@latest"; then
    echo "Update completed: ${pkg}"
    return 0
  fi
  echo "Update failed for ${pkg}. Check permission and network." >&2
  return 1
}

update_cc_connect() {
  local latest_tag
  latest_tag="$(git ls-remote --tags --refs https://github.com/chenhg5/cc-connect.git "refs/tags/v*" 2>/dev/null | awk -F/ '{print $3}' | sort -V | tail -n 1 || true)"
  if [[ -z "${latest_tag}" ]]; then
    echo "Unable to get latest cc-connect tag." >&2
    return 1
  fi

  if ! have_cmd go; then
    echo "go is required to update cc-connect from source." >&2
    echo "Set INSTALL_GO_RUNTIME=true and recreate container, then retry." >&2
    return 1
  fi

  local tmpdir
  tmpdir="$(mktemp -d)"
  local outbin="${tmpdir}/cc-connect.new"

  echo "Building cc-connect from ${latest_tag}..."
  if ! git clone --depth 1 --branch "${latest_tag}" https://github.com/chenhg5/cc-connect.git "${tmpdir}/src" >/dev/null 2>&1; then
    rm -rf "${tmpdir}"
    echo "Failed to clone cc-connect source." >&2
    return 1
  fi

  (
    set -e
    cd "${tmpdir}/src"
    go build -ldflags='-w -s' -o "${outbin}" ./cmd/cc-connect || \
    go build -ldflags='-w -s' -o "${outbin}" . || \
    go build -ldflags='-w -s' -o "${outbin}" ./...
  ) >/dev/null 2>&1 || {
    rm -rf "${tmpdir}"
    echo "Failed to build cc-connect." >&2
    return 1
  }

  if [[ "$(id -u)" -eq 0 ]]; then
    install -m 0755 "${outbin}" /usr/local/bin/cc-connect
  elif have_cmd sudo; then
    sudo install -m 0755 "${outbin}" /usr/local/bin/cc-connect
  else
    rm -rf "${tmpdir}"
    echo "Need root privilege to install /usr/local/bin/cc-connect." >&2
    return 1
  fi

  rm -rf "${tmpdir}"
  echo "cc-connect updated to ${latest_tag}"
  return 0
}

update_tool_by_index() {
  local idx="$1"
  local display key source
  IFS='|' read -r display key source <<<"${TOOLS[$idx]}"

  if [[ "${TOOL_CAN_UPDATE[$idx]:-0}" != "1" ]]; then
    echo "No update available for ${display}."
    return 0
  fi

  if [[ "${source}" == "npm" ]]; then
    update_npm_tool "${key}"
  else
    update_cc_connect
  fi
}

